#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int in;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	in = ((unsigned)n);
	if (in >= 10)
		ft_putnbr_fd(in / 10, fd);
	ft_putchar_fd(in % 10 + '0', fd);
}
